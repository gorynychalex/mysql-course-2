-- ----------------------------------------------------------------------------
-- 1.1. Простые запросы
-- ----------------------------------------------------------------------------

-- Показать все вопросы
SELECT * FROM questions;

-- Показать все варианты ответов
SELECT * FROM answers;

-- Показать только текст вопросов
SELECT question_text FROM questions;

-- Показать вопросы с категориями
SELECT question_text, category FROM questions;

-- ----------------------------------------------------------------------------
-- 1.2. Фильтрация данных (WHERE)
-- ----------------------------------------------------------------------------

-- Показать вопросы по категории
SELECT * FROM questions WHERE category = 'SQL Basics';

-- Показать вопросы по сложности
SELECT * FROM questions WHERE difficulty = 'easy';

-- Показать вопросы из нескольких категорий
SELECT * FROM questions 
WHERE category IN ('SQL Basics', 'DDL', 'DML');

-- Показать вопросы средней и высокой сложности
SELECT * FROM questions 
WHERE difficulty IN ('medium', 'hard');

-- ----------------------------------------------------------------------------
-- 1.3. Сортировка результатов (ORDER BY)
-- ----------------------------------------------------------------------------

-- Сортировка по категории
SELECT * FROM questions ORDER BY category;

-- Сортировка по сложности (по убыванию)
SELECT * FROM questions ORDER BY difficulty DESC;

-- Сортировка по нескольким полям
SELECT * FROM questions 
ORDER BY category ASC, difficulty DESC;

-- ----------------------------------------------------------------------------
-- 1.4. Агрегатные функции и группировка
-- ----------------------------------------------------------------------------

-- Подсчитать количество вопросов по категориям
SELECT 
    category, 
    COUNT(*) AS question_count
FROM questions
GROUP BY category;

-- Подсчитать количество вопросов по сложности
SELECT 
    difficulty, 
    COUNT(*) AS question_count
FROM questions
GROUP BY difficulty;

-- Подсчитать количество вариантов ответов для каждого вопроса
SELECT 
    q.question_text, 
    COUNT(a.id) AS answer_count
FROM questions q
LEFT JOIN answers a ON q.id = a.question_id
GROUP BY q.id, q.question_text;

-- Найти количество правильных ответов на каждый вопрос
SELECT 
    q.question_text,
    SUM(CASE WHEN a.is_correct = TRUE THEN 1 ELSE 0 END) AS correct_answers
FROM questions q
LEFT JOIN answers a ON q.id = a.question_id
GROUP BY q.id, q.question_text;

-- ----------------------------------------------------------------------------
-- 1.5. Объединение таблиц (JOIN)
-- ----------------------------------------------------------------------------

-- Показать все варианты ответов для вопроса
SELECT 
    q.question_text, 
    a.answer_text, 
    a.is_correct
FROM questions q
JOIN answers a ON q.id = a.question_id
WHERE q.id = 1;

-- Показать правильные ответы на все вопросы
SELECT 
    q.question_text, 
    a.answer_text
FROM questions q
JOIN answers a ON q.id = a.question_id
WHERE a.is_correct = TRUE;

-- Показать все вопросы с количеством ответов
SELECT 
    q.id,
    q.question_text,
    q.category,
    COUNT(a.id) AS total_answers,
    SUM(CASE WHEN a.is_correct THEN 1 ELSE 0 END) AS correct_answers
FROM questions q
LEFT JOIN answers a ON q.id = a.question_id
GROUP BY q.id, q.question_text, q.category;

-- ============================================================================
-- ЧАСТЬ 2: МОДИФИКАЦИЯ СТРУКТУРЫ (DDL)
-- ============================================================================

-- ----------------------------------------------------------------------------
-- 2.1. Добавление новых столбцов (ALTER TABLE ADD)
-- ----------------------------------------------------------------------------

-- Добавить столбец для очков за вопрос
ALTER TABLE questions 
ADD COLUMN points INT DEFAULT 1;

-- Проверить изменения
DESCRIBE questions;

-- ----------------------------------------------------------------------------
-- 2.2. Изменение типа данных столбца (ALTER TABLE MODIFY)
-- ----------------------------------------------------------------------------

-- Изменить тип данных для более точных значений
ALTER TABLE questions 
MODIFY COLUMN points DECIMAL(3,1) DEFAULT 1.0;

-- Проверить изменения
DESCRIBE questions;

-- ----------------------------------------------------------------------------
-- 2.3. Создание индексов
-- ----------------------------------------------------------------------------

-- Индекс для ускорения поиска по категории
CREATE INDEX idx_category ON questions(category);

-- Индекс для ускорения фильтрации по сложности
CREATE INDEX idx_difficulty ON questions(difficulty);

-- Составной индекс для частых запросов
CREATE INDEX idx_category_difficulty ON questions(category, difficulty);

-- Проверить созданные индексы
SHOW INDEX FROM questions;

-- ============================================================================
-- ЧАСТЬ 3: ОБНОВЛЕНИЕ ДАННЫХ (UPDATE)
-- ============================================================================

-- ----------------------------------------------------------------------------
-- 3.1. Простое обновление
-- ----------------------------------------------------------------------------

-- Обновить очки для сложных вопросов
UPDATE questions 
SET points = 3.0 
WHERE difficulty = 'hard';

-- Обновить очки для средних вопросов
UPDATE questions 
SET points = 2.0 
WHERE difficulty = 'medium';

-- Проверить результаты
SELECT question_text, difficulty, points FROM questions;

-- ----------------------------------------------------------------------------
-- 3.2. Обновление нескольких столбцов
-- ----------------------------------------------------------------------------

-- Обновить категорию и очки
UPDATE questions 
SET category = 'SQL Fundamentals', points = 1.5
WHERE category = 'SQL Basics';

-- Проверить результаты
SELECT question_text, category, points FROM questions;

-- ----------------------------------------------------------------------------
-- 3.3. Обновление с условием
-- ----------------------------------------------------------------------------

-- Увеличить очки на 0.5 для вопросов с определённым ID
UPDATE questions 
SET points = points + 0.5
WHERE id BETWEEN 1 AND 5;

-- Проверить результаты
SELECT id, question_text, points FROM questions WHERE id BETWEEN 1 AND 5;

